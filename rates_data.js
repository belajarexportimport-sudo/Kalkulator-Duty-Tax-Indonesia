// Helper: Parse the data string
function parseRateString(str) {
    const lines = str.trim().split('\n');
    const result = {
        // Output: zone -> { weight_key -> price }
    };

    // Initialize zones 1-10
    for (let z = 1; z <= 10; z++) {
        result[z] = {};
    }

    lines.forEach(line => {
        // Clean up common issues: "1,0", tabs
        let parts = line.replace(/\r/g, '').split(/[\t\s]+/); // Split by tabs OR spaces (consecutive)
        // Check if line corresponds to a data row (starts with number or pattern)
        if (parts.length < 2) return;

        // Skip header lines (containing "Zone")
        if (line.toLowerCase().includes('zone')) return;

        // Parse raw Weight Key
        let rawWeight = parts[0];
        // Clean quotes and newlines from copy-paste
        rawWeight = rawWeight.replace(/["\n]/g, '').trim();

        // Check for Minimum Rate Row
        if (rawWeight.toLowerCase().includes('minimum') || rawWeight.toLowerCase().includes('min')) {
            // Handle Minimum Rate Row
            for (let i = 0; i < 10; i++) {
                if (parts[i + 1]) {
                    let priceStr = parts[i + 1].replace(/\./g, '').replace(',', '.').replace(/[^0-9.]/g, '');
                    let price = parseFloat(priceStr);
                    if (!isNaN(price)) {
                        result[i + 1]['min'] = price;
                    }
                }
            }
            return; // Skip normal weight processing for this row
        }

        let weightKey;
        // Determine type of weight key
        if (rawWeight.includes('above') || rawWeight.includes('+')) {
            weightKey = '300'; // Max bracket
        } else if (rawWeight.includes('-')) {
            let range = rawWeight.split('-');
            weightKey = parseFloat(range[1]);
        } else {
            // "1,0" -> 1.0
            weightKey = parseFloat(rawWeight.replace(',', '.'));
        }

        // Loop through zones (Columns 1 to 10)
        // parts[1] is Zone 1, parts[2] is Zone 2...
        // Note: Some lines might have extra garbage.
        for (let i = 0; i < 10; i++) {
            if (parts[i + 1]) {
                let priceStr = parts[i + 1].replace(/["\n]/g, '').trim();
                let price = parseInt(priceStr.replace(/\./g, '')); // "1.205.904" -> 1205904

                // Add to result
                if (!isNaN(price) && !isNaN(weightKey)) {
                    // Check if this is a per-kg rate row
                    // User says >20kg is "per 1 kg" or "per 0.5kg" (but usually flat if large?)
                    // The table says "For shipment weight above 20 kg (Price per kg...)"
                    // So if weightKey > 20, store as rate?
                    // The lookup logic needs to know.
                    // I will store exactly as is. The lookup function handle 20+ logic.

                    result[i + 1][weightKey] = price;
                }
            }
        }
    });

    return result;
}

